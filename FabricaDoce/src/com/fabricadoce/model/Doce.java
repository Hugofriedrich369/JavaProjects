package com.fabricadoce.model;

public class Doce {
	public String NomeDoce;
	public Double PesoAcucar;
	
	public String getNomeDoce() {
		return NomeDoce;
	}
	public void setNomeDoce(String nomeDoce) {
		NomeDoce = nomeDoce;
	}
	public Double getPesoAcucar() {
		return PesoAcucar;
	}
	public void setPesoAcucar(Double pesoAcucar) {
		PesoAcucar = pesoAcucar;
	}
}
